export async function getServerSideProps() {
  const client = new ApolloClient({
    uri: "https://rveapiql.herokuapp.com/graphql",
    cache: new InMemoryCache(),
  });

  const { data } = await client.query({
    query: gql`
      query {
        posts {
          id
          description
          createdAt
          category
          slug
          image
          title
        }
      }
    `,
  });

  return {
    props: {
      posts: data.posts,
    },
  };
}
